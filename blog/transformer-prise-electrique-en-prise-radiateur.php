<h2 id="etapes">Comment transformer une prise en prise radiateur — les étapes</h2>

<p><strong>Avant tout : coupez l'alimentation au disjoncteur et vérifiez l'absence de tension avec un testeur avant de toucher quoi que ce soit.</strong></p>

<h3>Cas n°1 : le circuit existant est compatible</h3>

