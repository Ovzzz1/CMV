<h2 id="prise-standard">Peut-on brancher un radiateur sur une prise standard ?</h2>

<p>Techniquement, votre radiateur se branchera sur votre prise. Mais l'installation sera non conforme et dangereuse. La raison est simple : un radiateur électrique fixe de 1 500 à 2 500 W fonctionne souvent plusieurs heures par jour, en continu. Ce n'est pas le même profil de consommation qu'un aspirateur ou un sèche-cheveux qu'on utilise cinq minutes.</p>

<p>Les prises murales standard sont alimentées par des circuits prévus pour une consommation intermittente — plusieurs appareils qui tournent chacun quelques minutes. Le câble qui alimente ces prises est généralement en 1,5 mm² de section, dimensionné pour un usage polyvalent de courte durée. Un radiateur fixe impose une charge soutenue que ce câble ne peut pas absorber sans risque.</p>

<p>La norme NF C 15-100 est formelle : chaque radiateur électrique fixe doit être alimenté par un circuit dédié, indépendant des autres prises. Pas une prise partagée, pas un prolongateur — un circuit propre depuis le tableau.</p>

<h2 id="danger">Pourquoi c'est dangereux — et ce que risque concrètement votre logement</h2>

<h3>Le câble sous-dimensionné : l'origine de tous les problèmes</h3>

<p>Un câble de section 1,5 mm² (câble standard prise murale) supporte au maximum 16 A en régime permanent, soit environ 3 680 W — sur le papier. En pratique, dès qu'un radiateur de 2 000 W tourne plusieurs heures sur ce circuit, avec d'autres appareils en parallèle sur la même ligne, la gaine du câble chauffe. Pas assez pour déclencher le disjoncteur immédiatement, mais assez pour dégrader progressivement l'isolation et créer un risque de court-circuit dans la cloison ou sous le parquet.</p>

