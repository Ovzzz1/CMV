<p><strong>Alerte : Peut-on utiliser de la Javel sur le parquet ?</strong><br>
C'est une recherche très fréquente, mais il faut le déconseiller formellement ! Non seulement l'eau de Javel risque de décolorer et de détruire irrémédiablement le bois de votre parquet, mais son efficacité contre les puces est très limitée. Elle ne parvient pas à pénétrer la membrane protectrice des cocons pour tuer les nymphes en développement. Oubliez la Javel et privilégiez les produits spécifiquement conçus pour cet usage.</p>

<h2 id="source">4. N'oubliez pas de traiter la source (animaux et extérieur)</h2>

<p>Vous aurez beau astiquer votre maison du sol au plafond, cela ne servira strictement à rien si votre chat ou votre chien vous ramène de nouveaux parasites. Traitez vos animaux de compagnie avec une pipette, un collier ou un comprimé vétérinaire. Si l'infestation provient de votre jardin, pensez à tondre régulièrement l'herbe et envisagez d'y pulvériser des nématodes, des vers microscopiques qui sont les prédateurs naturels de la puce.</p>

