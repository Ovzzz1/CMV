<h3>Les solutions naturelles (et les odeurs qu'elles détestent)</h3>
<p>Les puces ont un odorat très développé et détestent viscéralement certaines senteurs. Vous pouvez utiliser le sel de mer fin, en le saupoudrant sur le sol et les tapis. C'est un déshydratant puissant qui va littéralement assécher les œufs et les larves ; laissez-le agir quelques heures avant d'aspirer. La terre de diatomée est également redoutable : cette poudre naturelle agit comme des lames de rasoir microscopiques qui abîment la carapace des insectes. Enfin, diluez quelques gouttes d'huile essentielle de lavande, d'eucalyptus ou de citronnelle dans votre eau de lavage, car ces odeurs agissent comme d'excellents répulsifs.</p>

<h3>La térébenthine : la recette de grand-mère ultime</h3>
<p>C'est probablement l'astuce la plus redoutable pour sauver un vieux plancher en bois. L'essence de térébenthine détruit efficacement les œufs et les puces adultes en les desséchant. Appliquez le produit sur l'ensemble de la surface à l'aide d'un chiffon ou d'un pinceau pour bien faire pénétrer le liquide dans les rainures et les plinthes. Attention cependant : c'est un produit très fort. Portez impérativement des gants et un masque, et aérez longuement la pièce pendant et après le traitement.</p>

<h3>Le fumigène (Fogger) pour les infestations sévères</h3>
<p>Si malgré vos efforts l'invasion est hors de contrôle, il va falloir utiliser un insecticide puissant. Le fogger est une bombe aérosol qui va diffuser un brouillard de produit biocide et larvicide dans toute la pièce, atteignant ainsi les moindres fissures inaccessibles. Il est impératif de respecter les consignes : vous devez évacuer les lieux (humains et animaux), couvrir les aliments, et laisser agir plusieurs heures avant d'aérer en grand.</p>

