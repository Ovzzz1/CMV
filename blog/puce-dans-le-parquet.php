<h2 id="comprendre">1. Qu'est-ce qu'une puce de parquet et où se cache son "nid" ?</h2>

<p>Avant de dégainer les traitements, il faut comprendre votre ennemi pour frapper au bon endroit et ne pas gaspiller votre énergie sur des zones saines.</p>

<h3>Puce classique vs Puce de parquet : quelle différence ?</h3>
<p>Pour être totalement franc, la fameuse "puce de plancher" n'est pas une espèce mutante. Il s'agit en réalité de la puce commune, le plus souvent la puce du chat (<em>Ctenocephalides felis</em>) ou du chien, qui a simplement élu domicile dans votre sol. En effet, l'insecte adulte pond sur votre animal de compagnie, mais ces œufs ne sont pas collants. Par conséquent, ils glissent du pelage et tombent directement sur le sol. C'est là qu'ils éclosent pour devenir des larves, puis tissent un cocon pour se transformer en nymphes, avant de bondir sur vos jambes lorsqu'elles détectent vos vibrations et votre chaleur corporelle.</p>

<h3>Je n'ai pas d'animaux : pourquoi mon parquet est-il infesté ?</h3>
<p>C'est une situation très frustrante, mais tout à fait courante. Si vous n'avez ni chat ni chien, vous vous demandez légitimement ce qui les attire. En réalité, vous avez pu ramener des œufs ou une puce adulte depuis l'extérieur, simplement coincés sous la semelle de vos chaussures ou sur vos vêtements. De plus, les cocons de puces sont capables de survivre en état de dormance pendant plusieurs mois sans se nourrir. Ainsi, si les anciens locataires de votre maison possédaient un animal infesté, l'éclosion a pu se déclencher à votre arrivée suite aux vibrations de vos pas. Enfin, n'écartez pas la piste des rongeurs (souris, rats dans les murs) ou de la faune sauvage comme un hérisson dans votre jardin.</p>

<h3>Où chercher les nids dans votre maison ?</h3>
<p>Inutile d'inspecter le centre de votre salon si la surface est parfaitement lisse, car les puces de parquet ne survivent pas dans les espaces ouverts et exposés à la lumière. Elles fuient le soleil et cherchent l'obscurité. Leurs nids se trouvent exclusivement dans les interstices et les zones d'ombre. Vous devez donc traquer les petites taches noires (leurs excréments constitués de sang séché) dans les rainures et les fentes du bois, juste sous les plinthes, sous vos meubles lourds, et bien sûr, au cœur des fibres de vos tapis et moquettes.</p>

