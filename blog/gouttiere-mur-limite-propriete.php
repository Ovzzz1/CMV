<p>La gestion des eaux de pluie repose sur des articles du Code civil qui n'ont pas changé : vous êtes responsable de ce qui tombe de votre toit.</p>

<h3>L'article 681 : la servitude d'égout des toits</h3>
<p>Cet article stipule que tout propriétaire doit établir ses toits de manière que les eaux pluviales s'écoulent sur son propre terrain ou sur la voie publique. Il est formellement interdit de faire verser l'eau sur le fonds de son voisin. Cette règle s'applique à tous les types de bâtiments : maison, garage ou abri de jardin.</p>
