    <h2 id="technique-liaison">La technique de pro pour lier la surélévation à l'ancien mur</h2>

    <p>L'erreur classique du bricoleur du dimanche : nettoyer vaguement le haut du muret, poser une couche de mortier et empiler les blocs. Une belle fissure horizontale apparaît l'année suivante, pile à la jonction. L'ancien et le nouveau mur travaillent différemment sous les contraintes thermiques et mécaniques. Il faut les coudre ensemble, pas juste les coller.</p>

    <h3 id="etape-1-fers-ancrage">Étape 1 : Le piège du parpaing creux (comment bien ancrer ses fers ?)</h3>

    <p>Pour solidariser les deux parties, vous devez prolonger les poteaux raidisseurs verticaux (idéalement tous les 2,50 m à 3 m). C'est là que beaucoup se font piéger. Vous percez le haut de votre mur existant, vous préparez votre cartouche de scellement chimique... et vous tombez sur du vide. <strong>Un scellement chimique ne tient pas dans l'air d'une alvéole creuse.</strong> La résine n'a rien à mordre.</p>

    <figure>
        <img src="/assets/img/blog/rehausser-un-mur-en-parpaing-existant4.webp" alt="Illustration technique macro : sommet cassé d'un parpaing creux avec une tige de fer à béton insérée verticalement dans l'alvéole en cours de remplissage au béton liquide" width="900" height="506" loading="lazy">
        <figcaption>Le scellement chimique dans une alvéole vide ne tient pas. Il faut remplir la cellule de béton pour créer un ancrage massif autour du fer.</figcaption>
    </figure>

    <p>La vraie technique :</p>
    <ol>
        <li>Cassez le sommet des alvéoles à l'aide d'un marteau et d'un burin (ou d'un perforateur en mode burinement) pour ouvrir l'accès.</li>
        <li>Descendez un fer à béton de 10 mm de diamètre sur au moins <strong>40 cm de profondeur</strong> dans l'ancien mur.</li>
        <li>Coulez un béton liquide (gâché bien fluide) directement dans l'alvéole pour noyer le fer sur toute la hauteur. Laissez durcir 24 h minimum. Vous avez maintenant un ancrage indestructible.</li>
    </ol>

    <h3 id="etape-2-lit-mortier">Étape 2 : Le lit de mortier de rattrapage pour un alignement parfait</h3>

    <p>Les vieux murs ne sont jamais parfaitement droits. Après des années d'intempéries et de cycles gel/dégel, le sommet penche légèrement par endroits. Si vous posez votre première rangée de parpaings sur cette surface irrégulière, tous vos joints vont partir de travers — et vous ne pourrez plus rattraper le niveau ensuite.</p>

    <figure>
        <img src="/assets/img/blog/rehausser-un-mur-en-parpaing-existant5.webp" alt="Gros plan sur une truelle étalant un lit de mortier épais de 2 à 3 cm sur le sommet irrégulier d'un vieux mur en parpaing, cordeau de maçon tendu au-dessus pour guider l'alignement" width="900" height="506" loading="lazy">
        <figcaption>Un lit de mortier épais sur la première rangée absorbe les défauts de l'ancien mur. Le cordeau tendu garantit un départ parfaitement de niveau.</figcaption>
    </figure>

    <p>Sur la première rangée, n'hésitez pas à charger votre lit de mortier. Une épaisseur de 2 à 3 centimètres vous permet d'absorber les défauts et de repartir sur une base parfaitement horizontale. <strong>Tendez un cordeau entre les deux extrémités du mur</strong> et contrôlez avec le niveau à bulle avant que le mortier prenne. C'est cette rangée qui conditionne tout le reste.</p>

    <h3 id="etape-3-blocs-bancher">Étape 3 : L'astuce des blocs à bancher pour le passage des armatures</h3>

    <p>Une fois vos fers en attente plantés et dressés vers le ciel, vous allez vite comprendre le problème : soulever un parpaing de 20 kg par-dessus une tige de ferraille de 80 cm, les bras tendus, en essayant de le faire glisser sans renverser votre lit de mortier... c'est épuisant et approximatif. L'astuce de maçon : utilisez des <strong>blocs à bancher</strong> (ou parpaings d'angle évidés). Au lieu de les soulever par-dessus le fer, vous les faites simplement glisser latéralement dans le lit de mortier. Le fer passe dans l'évidement sans effort. Moins de fatigue, plus de précision dans la pose.</p>

    <h3 id="etape-4-chainage">Étape 4 : Le chaînage horizontal final (obligatoire)</h3>

    <p>Ne terminez jamais un rehaussement par une rangée de parpaings creux standards. La dernière ligne doit impérativement être réalisée avec des <strong>parpaings en U</strong> — des blocs creusés en forme de gouttière sur toute leur longueur.</p>

    <figure>
        <img src="/assets/img/blog/rehausser-un-mur-en-parpaing-existant6.webp" alt="Vue plongeante sur une rangée de parpaings en U avec armature triangulaire métallique posée à l'intérieur de la gouttière, prête à être coulée en béton pour le chaînage horizontal" width="900" height="506" loading="lazy">
        <figcaption>Les parpaings en U forment une gouttière continue dans laquelle on place les armatures horizontales avant de couler le béton. C'est la ceinture qui tient tout le mur.</figcaption>
    </figure>

    <p>Dans cette gouttière, vous déposez des armatures horizontales (des fers torsadés ou des cadres triangulaires), raccordées par ligature aux fers verticaux. Vous coulez ensuite du béton pour tout noyer. <strong>Cette ceinture périphérique soude l'ouvrage</strong> et l'empêche de s'ouvrir sous la pression du vent ou des variations thermiques saisonnières.</p>

    <h2 id="mur-perpendiculaire">Raccorder un nouveau mur perpendiculaire (harpage ou fers ?)</h2>

    <p>Le cas se présente souvent quand on veut créer un angle ou prolonger un muret pour fermer un espace. La première idée — disquer les anciens blocs en escalier pour faire un harpage (l'imbrication des deux maçonneries comme des marches) — est tentante mais laborieuse, et vous risquez d'ébranler la structure existante.</p>

    <p>La méthode plus propre : tracez une ligne verticale au cordeau sur l'ancien mur à l'endroit du raccord. Percez des trous tous les deux rangs dans les joints horizontaux. Scellez chimiquement des fers de liaison horizontaux de 8 ou 10 mm sur une profondeur de 15 à 20 cm. Venez ensuite buter vos nouveaux blocs contre l'ancien mur en noyant ces fers de liaison dans vos joints de mortier. La jonction est rigide, le chantier est propre, et vous n'avez pas touché à la structure d'origine.</p>

    <h2 id="faq">FAQ : cas particuliers et budget</h2>

    <h3>Comment surélever un mur de parpaings déjà crépi ?</h3>

    <p>Le mortier n'adhère pas sur un enduit de façade existant. Si vous posez vos nouveaux blocs sur le crépi sans retirer ce dernier, la jonction se décollera dans les deux à trois ans. Vous devez retrouver la maçonnerie brute sur la zone de contact.</p>

    <figure>
        <img src="/assets/img/blog/rehausser-un-mur-en-parpaing-existant7.webp" alt="Personne utilisant une meuleuse avec disque diamant pour entailler horizontalement l'enduit crépi d'un mur, faisant apparaître le parpaing brut gris dessous, nuage de poussière blanche" width="900" height="506" loading="lazy">
        <figcaption>La meuleuse avec disque diamant permet de découper proprement la bande d'enduit sans toucher à la maçonnerie en dessous.</figcaption>
    </figure>

    <p>Prenez une meuleuse avec un disque diamant et découpez une bande horizontale à environ 10 cm sous le sommet du mur. Burinez et retirez le crépi de cette bande. Votre première couche de mortier accrochera directement sur le parpaing mis à nu. Pour masquer la cicatrice après les travaux, noyez une trame en fibre de verre à la jonction lors de la remise en enduit — elle absorbera les tensions et évitera la fissure horizontale.</p>

