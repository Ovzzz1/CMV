<h2 id="preparation">4. Préparer le carrelage avant d'appliquer la résine (étape cruciale)</h2>
<p>C'est l'étape la plus sous-estimée et pourtant la principale cause d'échec. Un carrelage mal préparé garantit un décollage de la résine en quelques semaines. Voici le protocole complet.</p>

<h3>Étape 1 — Vérifier l'état du carrelage</h3>
<ol style="margin-bottom: 20px; padding-left: 20px;">
    <li><strong>Tapotez chaque carreau</strong> avec un objet dur : un son creux signifie que le carreau est décollé. Ces carreaux doivent être recollés (ou déposés) avant toute application.</li>
    <li><strong>Mesurez l'humidité résiduelle</strong> de la dalle avec un hygromètre de surface : elle doit être inférieure à 5 %. Un sol encore humide empêche l'adhérence et provoque des cloques. Voir notre article sur le <a href="https://www.cemarenov.fr/traitement-humidite">traitement de l'humidité</a> si le problème vient des murs.</li>
    <li><strong>Évaluez l'état des joints</strong> : joints fissurés, noirs ou proéminents doivent être traités avant l'application.</li>
</ol>

<h3>Étape 2 — Poncer et dégraisser la surface</h3>
<ol style="margin-bottom: 20px; padding-left: 20px;">
    <li><strong>Poncez mécaniquement</strong> le carrelage au disque diamant (ponceuse orbitale ou monobrosse équipée d'un grain 40-80) pour créer une accroche sur l'émail. Sur grès cérame très poli, cette étape est indispensable, sans ponçage, la résine ne tient pas.</li>
    <li><strong>Dépoussiérez soigneusement</strong> à l'aspirateur puis avec un chiffon non pelucheux légèrement humide.</li>
    <li><strong>Dégraissez</strong> avec un nettoyant dégraissant spécial support (acétone, alcool isopropylique ou produit dédié). Toute trace de graisse, de cire ou de silicone rend l'accrochage impossible.</li>
    <li><strong>Laissez sécher</strong> minimum 2-4h à température ambiante (idéalement entre 15 et 25°C).</li>
</ol>

<h3>Étape 3 — Traiter les joints</h3>
<ol style="margin-bottom: 20px; padding-left: 20px;">
    <li>Si les joints sont profonds ou très marqués : remplissez-les avec un enduit de rebouchage spécial résine ou un primaire chargé pour obtenir une surface la plus plane possible.</li>
    <li>Si les joints sont sains mais apparents : une couche de primaire époxy d'adhérence bien appliquée peut les masquer partiellement. Pour un rendu totalement lisse (sans trace de joints), il faut impérativement une phase d'égalisation avec un enduit autolissant avant la résine.</li>
</ol>

<h2 id="application">5. Comment appliquer la résine époxy sur carrelage : les étapes</h2>
<p>La résine époxy est un produit bicomposant : vous mélangez une résine (composant A) avec un durcisseur (composant B) dans des proportions strictes. Ce mélange déclenche une réaction chimique irréversible, vous avez un temps de pot (30 min à 2h selon les produits) pour l'appliquer.</p>

<h3>Étape 1 — Appliquer le primaire d'adhérence (si nécessaire)</h3>
<ol style="margin-bottom: 20px; padding-left: 20px;">
    <li>Mélangez le primaire époxy selon les instructions du fabricant (ratio résine/durcisseur, souvent 2:1 ou 3:1 en poids).</li>
    <li>Appliquez au rouleau courte mèche (5-8 mm) sur l'ensemble du carrelage poncé et dépoussiéré.</li>
    <li>Laissez sécher jusqu'à l'état "poisseux sec" (généralement 4-8h selon la température), ne laissez pas dépasser le temps de recouvrabilité indiqué, sinon la couche suivante n'accrochera plus.</li>
</ol>

<h3>Étape 2 — Mélanger la résine de finition</h3>
<ol style="margin-bottom: 20px; padding-left: 20px;">
    <li><strong>Pesez exactement</strong> les composants A et B selon le ratio pondéral du fabricant (jamais volumétrique, la densité des deux composants diffère).</li>
    <li><strong>Mélangez lentement</strong> à la spatule ou au mélangeur électrique à faible vitesse (&lt;200 tr/min) pendant 3 minutes en raclant bien les bords et le fond du pot. Un mélange trop rapide incorpore des bulles d'air qui restent emprisonnées et opacifient la surface.</li>
    <li>Attendez 5 minutes (temps d'induction) pour que la réaction démarre avant d'appliquer.</li>
</ol>

<h3>Étape 3 — Appliquer les couches de finition</h3>
<ol style="margin-bottom: 20px; padding-left: 20px;">
    <li><strong>Première couche</strong> : versez la résine par zones et étalez au rouleau courte mèche. Travaillez en bandes parallèles en croix. Passez un rouleau à picots juste après pour chasser les éventuelles bulles.</li>
    <li><strong>Laissez sécher</strong> le temps indiqué (généralement 12-24h), respectez scrupuleusement le délai avant recouvrement.</li>
    <li><strong>Poncez légèrement au papier 220</strong> entre les couches pour améliorer l'accrochage (sauf indication contraire du fabricant).</li>
    <li><strong>Deuxième couche</strong> : même protocole. La plupart des résines de sol nécessitent 2 couches minimum sur carrelage (3 si les joints sont encore visibles).</li>
    <li><strong>Séchage total</strong> : praticable à pied sous 24-48h, mais la dureté définitive n'est atteinte qu'après 5 à 7 jours. Attendez avant de remettre du mobilier lourd.</li>
</ol>

