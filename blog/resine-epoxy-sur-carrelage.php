<h2 id="faisabilite">1. Peut-on vraiment appliquer une résine époxy sur du carrelage existant ?</h2>
<p>Oui, et c'est même l'un des cas d'usage les plus courants de la résine de sol. La résine époxy (ou polyuréthane) adhère sur un carrelage sans avoir à le casser ni à le retirer, ce qui permet de gagner du temps et de l'argent par rapport à une dépose totale. Il existe pour cela <strong>deux méthodes d'application</strong> :</p>
