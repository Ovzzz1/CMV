<h2 id="alternatives">8. Quelles alternatives à la résine époxy pour recouvrir un carrelage ?</h2>
<p>Si votre carrelage présente des carreaux décollés nombreux, une trop forte humidité résiduelle ou des joints trop marqués qui rendraient l'époxy inadapté, voici les alternatives :</p>
<ul class="custom-list">
    <li><strong>Béton ciré / microtopping</strong> : enduit décoratif en polymère + ciment, 3 mm d'épaisseur, rendu lisse sans joints. Idéal pour salle de bain ou cuisine. Prix : 60 à 150 €/m² posé.</li>
    <li><strong>Lames de vinyle LVT ou PVC</strong> : pose flottante ou collée directement sur le carrelage. Solution rapide, peu de préparation. Prix : 20 à 60 €/m².</li>
    <li><strong>Peinture carrelage monocomposante</strong> : moins durable que la résine époxy (3-5 ans), mais plus accessible et plus simple à appliquer. Idéal pour un carrelage mural en bon état.</li>
    <li><strong>Enduit décoratif ou tadelakt</strong> : pour les murs de salle de bain, des enduits imperméables à base de chaux peuvent recouvrir le carrelage mural sans dépose.</li>
</ul>

