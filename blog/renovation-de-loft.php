<h3>Quels sont les postes de dépenses les plus lourds ?</h3>

<p>Dans une réhabilitation industrielle, les coûts les plus importants sont souvent invisibles pour le néophyte. En effet, la mise en conformité des fluides, incluant la plomberie et l'électricité, peut représenter jusqu'à 25 % du budget total car les distances de raccordement dans de grands plateaux sont plus longues. Par contre, le remplacement des menuiseries, telles que les verrières monumentales ou les fenêtres de toit, ainsi que la réfection de la charpente, sont les éléments qui font réellement basculer le devis. C'est d'autant plus vrai si vous visez des performances énergétiques élevées car l'isolation de parois vitrées géantes coûte cher.</p>

<h2 id="prerequis">2. Les prérequis administratifs et techniques avant de commencer</h2>

<p>Un loft est, par définition légale, un changement de destination. Avant même de dessiner vos plans, vous devez impérativement valider la faisabilité de votre projet auprès de la mairie et de la copropriété.</p>

<ul class="custom-list">
    <li><strong>Le changement d'usage :</strong> Transformer un local commercial ou artisanal en habitation nécessite une autorisation d'urbanisme. Vérifiez systématiquement le PLU (Plan Local d'Urbanisme) car certaines zones interdisent la perte de surfaces d'activités économiques au profit du résidentiel.</li>
    <li><strong>La vérification des sols :</strong> Pour un ancien garage ou une usine, une étude de pollution est une étape non négociable. Toutefois, peu de futurs acquéreurs le savent : si le sol est pollué aux hydrocarbures ou aux métaux lourds, le coût du traitement des terres peut rendre l'opération immobilière non rentable.</li>
