<h2 id="double-peau">4. Montage en "Double Peau" : la solution pour l'acoustique et la rigidité</h2>

<p>Le principal reproche fait aux portes à galandage est l'effet "mur creux" et la faible isolation phonique. Puisque la cloison est vide pour laisser passer la porte, elle résonne comme une caisse de guitare. Pour corriger ce défaut, je conseille systématiquement de passer sur une cloison finie de 120 mm.</p>

<p>Pour y arriver, on utilise un châssis standard de 70 mm, mais au lieu d'une seule plaque de plâtre, on en visse deux de chaque côté (2 x BA13). C'est ce qu'on appelle un montage en double peau. Cette épaisseur supplémentaire change tout : la cloison devient extrêmement rigide, elle ne vibre plus lors de la manipulation de la porte, et l'atténuation acoustique est réelle. C'est la configuration idéale pour séparer une chambre d'un salon ou pour isoler des toilettes.</p>

