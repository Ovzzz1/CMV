<h2 id="erreur">5. Pour aller plus loin : l'erreur à ne pas commettre au montage</h2>

<p>Sachez que l'épaisseur de votre cloison ne servira à rien si le châssis n'est pas posé avec une précision chirurgicale. La valeur ajoutée d'un bon montage réside dans l'aplomb. Si votre châssis est incliné de seulement 2 mm sur sa hauteur, la porte aura tendance à s'ouvrir ou se fermer toute seule, ou pire, elle viendra frotter contre les plaques de plâtre à mi-course. L'épaisseur choisie doit inclure cette tolérance : plus votre vide interne est généreux (cloison de 100 mm au lieu de 95 mm), plus vous avez de marge de manœuvre pour compenser les petites imperfections du bâti.</p>

