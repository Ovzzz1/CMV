<h2 id="porte-chassis">3. Quelle épaisseur de porte pour quel châssis ? (Le piège du vide interne)</h2>

<p>Le choix de votre porte dicte directement l'épaisseur de la cloison, car le "vide interne" du châssis doit être suffisant pour absorber les mouvements naturels du matériau.</p>

