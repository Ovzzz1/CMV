<h2 id="tack-cloth">4. Le secret de l'ébéniste : Le chiffon poisseux (Tack Cloth)</h2>

<p>Si vous voulez vraiment un fini professionnel, vous devez investir dans un "Tack Cloth". C'est un chiffon imprégné d'une résine collante qui emprisonne la poussière au lieu de la déplacer. C'est l'outil que j'utilise juste avant de donner le premier coup de pinceau.</p>

<p>D'ailleurs, si vous êtes en rupture de stock, il existe une astuce "Système D" que j'ai récupérée sur L'Air du Bois : imbibez un vieux chiffon en coton propre avec un peu de vernis très dilué. Vous obtiendrez un chiffon collant maison tout aussi efficace. Pour valider mon support, je passe enfin un gant en latex blanc sur la surface : s'il reste immaculé, je sais que je suis prêt.</p>

<h2 id="erreurs">5. Retours d'expérience : Les erreurs de "bon sens" qui ruinent tout</h2>

<h3>Le recyclage des vieux T-shirts : Le piège de l'assouplissant</h3>
<p>C'est l'erreur la plus classique. On pense bien faire en utilisant un vieux vêtement propre, mais s'il a été lavé avec de la soupline, c'est la catastrophe assurée. L'assouplissant laisse un film gras invisible sur le tissu qui se transfère au bois lors du frottement. J'ai déjà vu des vernis entiers clouer avec ce fameux phénomène d'"œil de poisson" à cause de cette simple erreur de blanchisserie.</p>

<h3>Le White Spirit : Le résidu gras fatal</h3>
<p>Le White Spirit est souvent présenté comme un nettoyant universel, pourtant je le proscris formellement pour le nettoyage post-ponçage. Étant un dérivé pétrolier, il laisse une pellicule grasse qui met des heures à s'évaporer. Si vous appliquez une huile moderne comme le Rubio Monocoat par-dessus, le produit ne pourra pas se fixer moléculairement à la cellulose du bois et votre protection ne tiendra pas.</p>

<h2 id="specificites">6. Spécificités : Adapter son nettoyage au type de bois</h2>

<h3>Bois tanniques (Chêne, Châtaignier) : Attention aux taches noires</h3>
<p>Lorsque vous nettoyez ces bois, évitez absolument l'eau ou les éponges métalliques. Le contact entre le tanin, l'eau et le métal provoque des réactions chimiques qui créent des taches noires instantanées et indélébiles. Restez sur un nettoyage à l'alcool ou à l'acétone pour ces essences.</p>

<h3>Bois tendres (Sapin, Pin) : Gare à l'écrasement des fibres</h3>
<p>Le pin est très sensible. Un nettoyage trop vigoureux avec un chiffon rugueux ou une brosse trop dure peut marquer le bois avant même la première couche. Je traite toujours ces essences avec une douceur particulière car chaque micro-rayure de nettoyage se verra multipliée par dix une fois le vernis appliqué.</p>

