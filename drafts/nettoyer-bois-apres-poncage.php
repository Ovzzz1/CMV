<h3>L'alcool ménager : le roi du nettoyage "sec"</h3>
<p>C'est mon solvant de prédilection pour le chêne ou le châtaignier. L'alcool ménager à 95° s'évapore en moins de 30 secondes et présente l'avantage immense de ne pas relever les fibres du bois. C'est l'outil idéal pour capter les dernières poussières après l'aspiration tout en s'assurant que le bois reste parfaitement sec.</p>

<h3>L'acétone pour les bois gras (exotiques)</h3>
<p>Pour des bois comme le Teck, l'Iroko ou le Palissandre, l'alcool ne suffit pas. Ces essences contiennent des huiles naturelles qui empêchent l'adhérence des colles et des vernis. J'utilise alors l'acétone car c'est le seul solvant assez puissant pour ouvrir les pores en surface et "déshuiler" temporairement le bois. C'est le "tip" niché indispensable pour réussir un collage structurel sur du bois exotique.</p>

<h3>Le "Water Popping" : Relever la fibre volontairement</h3>
<p>Parfois, j'utilise l'eau, mais pas pour nettoyer. C'est une technique avancée appelée "Water Popping". En vaporisant une fine brume d'eau après le dernier grain de ponçage, je force les fibres à se redresser. Une fois sec, je repasse un coup de grain très fin pour les couper. Cette méthode permet d'obtenir une profondeur de teinte incroyable, surtout si vous utilisez des lasures ou des huiles colorées à l'eau.</p>

