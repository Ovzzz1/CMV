<h2 id="mecanique">2. Le protocole mécanique : Pourquoi votre aspirateur est peut-être votre pire ennemi</h2>

<h3>L'aspirateur avec embout brosse : l'indispensable</h3>
<p>L'aspiration est la première ligne de défense, néanmoins, tous les embouts ne se valent pas. Je bannis systématiquement l'embout suceur plat en plastique rigide car il a tendance à rayer les fibres tendres que vous venez de lisser. Par contre, j'utilise toujours une brosse à poils souples que je décrasse soigneusement au compresseur entre deux passes de nettoyage. Cela permet de "gratter" l'intérieur des pores pour en extraire la poussière sans agresser le bois.</p>

<h3>Le piège de la soufflette : une fausse bonne idée ?</h3>
<p>On voit souvent des artisans utiliser la soufflette à tout va, mais c'est un risque majeur. Sur Reddit, beaucoup d'ébénistes alertent sur le fait que l'air comprimé ne fait souvent que déplacer la poussière d'un point A vers un point B (généralement vos poumons ou le reste de votre atelier). Pire encore, si votre compresseur n'est pas équipé d'un filtre déshuileur performant, vous risquez d'envoyer des micro-gouttelettes d'huile sur votre bois brut, créant des zones de refus lors du vernissage.</p>

<h3>Le cas critique du MDF et des poussières volatiles</h3>
<p>Si vous travaillez du MDF, la donne change car la poussière est ici ultra-fine et toxique. Dans ce cas précis, la soufflette est interdite. La seule option viable pour ne pas saturer l'air de votre espace de travail est l'aspiration cyclonique de classe M, car elle garantit que les particules fines ne ressortent pas par l'échappement de l'aspirateur.</p>

