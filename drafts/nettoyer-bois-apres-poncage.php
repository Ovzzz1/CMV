<h2 id="etape-bacle">1. L'étape que tout le monde bâcle : Pourquoi 90 % des finitions ratées se jouent ici</h2>

<p>La plupart des bricoleurs pensent qu'une surface lisse au toucher est une surface prête à peindre, mais c'est un piège. La poussière de ponçage est vicieuse car elle s'insère dans les fibres ouvertes par l'abrasif et crée une barrière invisible entre le support et votre produit de finition.</p>

<p>D'ailleurs, j'utilise systématiquement le test de la lumière rasante pour valider mon travail. En plaçant une lampe puissante au ras de la planche, on s'aperçoit vite que ce que l'on pensait propre est en réalité tapissé d'un voile de particules fines. Si vous appliquez un vernis là-dessus, vous allez emprisonner cette poussière "fantôme" qui va remonter à la surface par capillarité, gâchant ainsi l'aspect miroir de votre ouvrage.</p>

