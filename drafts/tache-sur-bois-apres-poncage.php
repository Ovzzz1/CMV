<h3>Les taches noires ou grisâtres</h3>
<p>Ces marques sont les plus fréquentes sur les bois tanniques comme le chêne ou le châtaignier. Elles ne sont pas de la saleté, mais une réaction chimique entre les tanins du bois, l'humidité et le métal. Si vous avez utilisé de la laine d'acier avant de poncer, des micro-particules de fer ont pu rester logées dans les pores du bois et provoquer une oxydation immédiate. Par contre, si la tache est plus diffuse, il s'agit généralement d'une ancienne infiltration d'eau qui a fait moisir la fibre sous l'ancienne protection.</p>

<h3>Les auréoles blanches ou collantes</h3>
<p>Les zones blanchâtres indiquent souvent que le décapage précédent a été incomplet, car il s'agit de résidus de vieux vernis qui ont chauffé sous l'effet de l'abrasion. Néanmoins, ces marques peuvent aussi révéler des remontées de colle ancienne lors d'un assemblage précédent. Ces zones sont critiques puisque toute finition ultérieure comme une huile ou un vernis ne pourra pas pénétrer uniformément.</p>

<h3>Les traces de ponçage : ne confondez pas !</h3>
<p>Il est crucial de distinguer une tache d'une trace de ponçage. Les micro-rayures circulaires laissées par une ponceuse excentrique sont invisibles sur le bois sec, mais sautent aux yeux dès que l'on humidifie le support. Si vos marques suivent un schéma répétitif, le problème est mécanique : vous avez sauté trop de grains ou votre disque était encrassé. Dans ce cas, aucun produit chimique ne vous aidera car il faut reprendre le cycle de ponçage méthodiquement.</p>

