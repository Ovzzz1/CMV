<h2 id="enlever">2. Comment enlever les taches incrustées sur du bois brut ?</h2>

<p>Une fois le diagnostic posé, l'objectif est d'extraire la tache sans altérer la planéité de la surface. J'utilise personnellement deux approches selon la ténacité du défaut, tout en gardant à l'esprit que le bois est un matériau hydrophile qui réagit à chaque apport de liquide.</p>

<h3>Les astuces douces (bicarbonate, vinaigre et eau)</h3>
<p>Pour des taches d'eau superficielles, le bicarbonate de soude est une excellente première étape car il est peu abrasif. Je conseille de former une pâte avec un peu d'eau, de l'appliquer sur la zone et de frotter doucement dans le sens du fil du bois. Le vinaigre blanc est également utile pour neutraliser certaines réactions, mais attention car son acidité peut modifier la teinte de certaines essences. Une autre technique consiste à vaporiser un peu d'eau chaude sur l'ensemble de la surface afin de relever les fibres écrasées, ce qui permet de libérer des poussières coincées lors d'un dernier égrenage manuel.</p>

<h3>Le traitement choc : l'acide oxalique</h3>
<p>Si vous faites face à des taches noires d'oxydation, l'acide oxalique est le produit souverain. C'est un acide organique qui décolore les taches de tanin sans blanchir la fibre naturelle du bois. Je le dilue à raison de 100g par litre d'eau chaude, puis je l'applique à l'éponge sur toute la surface pour éviter les démarcations de zones. Après un temps de pose de 15 à 30 minutes, le rinçage est obligatoire car il faut stopper l'action de l'acide pour éviter que des cristaux ne fassent rejeter votre future finition.</p>

<p><strong>Attention au placage :</strong> Le placage est une feuille de bois noble de moins d'un millimètre. Si vous voyez une tache sur ce type de meuble, rangez votre ponceuse car vous risquez de percer la feuille. Travaillez exclusivement par voie chimique avec des compresses imbibées d'éclaircisseur.</p>

<h2 id="nettoyage">3. L'étape clé : le nettoyage du bois après ponçage</h2>

<p>Même si les taches ont disparu, le travail n'est pas terminé car le ponçage génère une poussière microscopique qui bouche les pores du bois. Aspirer ne suffit pas. Pour une finition professionnelle, je recommande de passer un chiffon non pelucheux imbibé d'acétone sur l'intégralité du bois. L'acétone s'évapore instantanément tout en ouvrant les pores et en dégraissant la surface. Cela permet de vérifier l'uniformité du bois : si une tache doit réapparaître sous le vernis, elle deviendra visible lors du passage du chiffon. C'est votre dernier test de sécurité avant la finition.</p>

