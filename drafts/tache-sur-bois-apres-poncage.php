<h2 id="decapage">4. Pour aller plus loin : l'importance d'un bon décapage en amont</h2>

<p>La plupart des problèmes de taches après ponçage trouvent leur source dans un décapage initial bâclé. Poncer n'est pas décaper puisque la chaleur de la ponceuse a tendance à cuire les anciens vernis plutôt qu'à les retirer. Pour éviter ces déboires, je préconise toujours l'utilisation d'un décapant chimique de qualité ou d'un racloir bien affûté avant d'attaquer le ponçage mécanique, ce qui garantit de travailler sur une fibre saine dès le départ.</p>

