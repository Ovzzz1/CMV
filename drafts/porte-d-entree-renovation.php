<h2 id="diagnostic">1. Diagnostic du bâti : pose en rénovation ou dépose totale ?</h2>

<p>C'est la question fondamentale que vous devez trancher avant même de contacter un menuisier : peut-on changer la porte sans changer le cadre ? La réponse courte est oui, mais seulement à condition que votre dormant actuel soit parfaitement sain et d'aplomb. Si vous optez pour la pose en rénovation, l'artisan vient visser le nouveau bâti directement sur l'ancien, ce qui permet une intervention rapide et propre sans dégradation des murs. Par contre, il faut absolument anticiper le piège de la "clarté de passage" puisque vous allez mécaniquement perdre environ 3 à 5 cm de largeur utile. Ce détail semble anodin mais peut rendre le passage d'une poussette ou d'un meuble volumineux extrêmement pénible au quotidien. Cependant, si le bois de l'ancien cadre présente des traces de pourriture ou que le métal est oxydé, la dépose totale devient inévitable car elle est la seule garante d'une isolation thermique réelle et d'une étanchéité pérenne.</p>

