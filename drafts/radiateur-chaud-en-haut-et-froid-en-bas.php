<h2 id="causes">Pourquoi un radiateur chauffe-t-il en haut mais reste froid en bas ?</h2>

<h3>L'air emprisonné dans le circuit</h3>

<p>C'est la cause la plus fréquente. Au fil des mois, de petites poches d'air s'accumulent dans le circuit de chauffage. Quand ces poches se logent dans un radiateur, elles forment une barrière qui ralentit le flux d'eau chaude — particulièrement vers la partie basse. L'eau circule en haut du radiateur et la chaleur s'y diffuse, mais le bas reste froid faute de circulation suffisante. La solution : purger le radiateur pour évacuer cet air.</p>

<h3>Les dépôts de boue et de calcaire</h3>

<p>Dans les installations de chauffage central vieillissantes, des dépôts se forment progressivement au fond des radiateurs. La boue — mélange d'oxyde de fer, de calcaire et de résidus — se dépose en priorité dans la partie inférieure, où l'eau circule le moins vite. Le bas du radiateur se retrouve obstrué et la chaleur ne peut plus s'y diffuser. Dans ce cas, la purge ne suffit pas : un désembouage du circuit s'impose.</p>

<h3>Un déséquilibre hydraulique ou une pression insuffisante</h3>

<p>Si la pression dans le circuit de chauffage est trop basse (inférieure à 1 bar), la pompe de circulation ne parvient pas à alimenter correctement tous les radiateurs, surtout ceux en bout de réseau ou à l'étage. L'eau chaude peine à atteindre la partie inférieure des radiateurs les plus éloignés. Un robinet thermostatique partiellement fermé peut produire le même effet en limitant l'arrivée d'eau chaude et en créant une circulation partielle à l'intérieur du radiateur.</p>

<h2 id="solutions">Comment résoudre le problème : les solutions dans l'ordre</h2>

<h3>1. Vérifier le robinet thermostatique</h3>

<p>Commencez toujours par là — c'est la vérification la plus rapide. Si le robinet thermostatique n'est pas complètement ouvert, l'eau chaude entre dans le radiateur en débit réduit et ne circule pas uniformément. Ouvrez-le en grand (position maximale) et attendez 30 minutes avant de diagnostiquer autre chose. Si le bas du radiateur commence à chauffer, le problème est résolu. Vérifiez aussi qu'aucun objet ne bloque physiquement la tête thermostatique.</p>

<h3>2. Purger le radiateur</h3>

<p>Si le robinet est bien ouvert et que le problème persiste, purgez le radiateur. Voici la procédure étape par étape :</p>

<ul class="custom-list">
<li>Éteignez la chaudière et attendez que le circuit soit tiède.</li>
<li>Repérez le purgeur — il se situe sur la partie haute du radiateur, sur le côté, sous forme d'une petite vis carrée ou hexagonale.</li>
<li>Placez un récipient sous le purgeur pour recueillir l'eau.</li>
<li>Desserrez la vis du purgeur avec un tournevis ou une clé de purge. Ne l'enlevez pas complètement.</li>
<li>Laissez l'air s'échapper — vous entendrez un sifflement. Dès qu'un filet d'eau sort en continu sans bulles d'air, refermez la vis.</li>
<li>Vérifiez ensuite la pression de la chaudière et rallumez le chauffage.</li>
</ul>

<p>Renouvelez la purge une à deux fois par an pour assurer un fonctionnement optimal du circuit de chauffage et éviter que ce problème ne revienne.</p>

<h3>3. Vérifier la pression de la chaudière</h3>

<p>Après une purge, la pression du circuit baisse. Vérifiez le manomètre de votre chaudière : la pression doit se situer entre 1 et 1,5 bar à froid (2 bar maximum en fonctionnement). Si elle est inférieure à 1 bar, regonflez le circuit via le robinet de remplissage jusqu'à atteindre 1,2 à 1,3 bar. Une pression insuffisante empêche l'eau chaude d'alimenter correctement la partie basse des radiateurs, en particulier ceux situés à l'étage.</p>

<h3>4. Désembouer le circuit de chauffage</h3>

<p>Si votre circuit n'a pas été entretenu depuis plusieurs années et que la purge n'a rien changé, les dépôts de boue calcaire sont probablement en cause. Le désembouage consiste à injecter un produit dans le circuit pour décoller et évacuer ces résidus. Cette intervention requiert un professionnel — comptez entre 300 et 600 € selon la taille de l'installation. Un désembouage est recommandé tous les 5 à 10 ans sur un circuit ancien, et systématiquement lors de l'installation d'une nouvelle chaudière sur un réseau existant.</p>

<h3>5. Remplacer le radiateur</h3>

<p>Si malgré toutes ces interventions le radiateur reste chaud en haut et froid en bas, sa structure interne est probablement trop endommagée pour être réhabilitée. Un radiateur dont les cloisons internes sont colmatées par des dépôts durcis ne peut plus assurer une circulation correcte de l'eau chaude. C'est le moment de le remplacer — un radiateur à panneaux en acier neuf diffuse la chaleur de façon homogène et permet de retrouver un confort thermique satisfaisant dans la pièce.</p>

<h2 id="equilibrage">Quand le problème touche plusieurs radiateurs : l'équilibrage hydraulique</h2>

<p>Si plusieurs radiateurs de votre logement présentent ce symptôme, ou si certaines pièces ne chauffent jamais correctement malgré le système de chauffage allumé, le problème n'est plus local — il est lié à l'équilibrage hydraulique de l'installation. Un circuit mal équilibré distribue l'eau chaude de façon inégale : les radiateurs proches de la chaudière reçoivent un débit excessif, ceux en bout de réseau pas assez. Un professionnel peut rééquilibrer le débit sur chaque radiateur en ajustant les vannes, ce qui améliore la circulation de l'eau sur l'ensemble du système de chauffage central et restaure un confort thermique homogène dans tout le logement.</p>
