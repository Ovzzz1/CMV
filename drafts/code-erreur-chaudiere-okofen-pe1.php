<h2 id="voute-silo">La voute de silo : cause méconnue, solution souvent simple</h2>

<p>La voute de silo est un phénomène physique fréquent sur les installations Ökofen à vis sans fin : les granulés, au lieu de descendre naturellement vers la vis d'extraction, forment une arche compacte dans le bas du silo. La vis tourne à vide, la chaudière ne reçoit plus de combustible, et le PE1 s'affiche — alors que le silo est en réalité bien rempli au-dessus de la voute.</p>

<p>Ce phénomène est favorisé par deux conditions : des granulés de petit calibre ou de qualité médiocre (poussière, humidité), et les silos textiles dont la forme favorise le pontage en fond de cuve.</p>

<p>La solution est mécanique et ne demande pas d'outil spécial : introduire une tige longue dans le silo par l'orifice d'accès et briser l'arche en effectuant des mouvements circulaires. Une fois la voute cassée, les granulés se remettent à couler librement. Réamorcer ensuite la vis sans fin et relancer la chaudière normalement.</p>

