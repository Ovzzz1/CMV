<h2 id="resolution">Comment résoudre le code erreur 611 — étapes dans l'ordre</h2>

<h3>Étape 1 — Inspecter la sortie extérieure de la ventouse</h3>

<p>C'est la première vérification à faire, et souvent la seule nécessaire. Repérez la sortie de ventouse sur la façade extérieure (généralement une grille ronde ou rectangulaire en façade ou en toiture). Vérifiez visuellement qu'aucun obstacle ne bloque l'entrée ou la sortie : feuilles, neige, glace, nid. Si un obstacle est présent, dégagez-le à la main ou avec une brosse douce. Ne pas utiliser d'objet rigide qui pourrait endommager la grille.</p>

<h3>Étape 2 — Vérifier la pression d'eau (pour exclure une confusion)</h3>

<p>La pression d'eau ne cause pas le code 611, mais vérifiez-la rapidement pour exclure un autre problème simultané. Le manomètre doit indiquer entre 1 et 1,5 bar en froid. Si la pression est normale, ce n'est pas la source du problème.</p>

<h3>Étape 3 — Contrôler le ventilateur d'extraction</h3>

<p>Si aucun obstacle extérieur n'est visible, le ventilateur peut être en cause. Sur certains modèles Chaffoteaux Mira C Green, le ventilateur est accessible après démontage du capot. Signes d'un ventilateur défaillant : bruit anormal, rotation visible mais lente, ou absence totale de rotation. Cette étape nécessite un minimum de connaissances techniques — si vous n'êtes pas à l'aise, passez directement à l'étape suivante.</p>

<h3>Étape 4 — Réinitialiser la chaudière</h3>

<p>Une fois l'obstacle éliminé, réinitialisez la chaudière en appuyant sur le bouton reset (souvent identifié par un symbole flamme barré ou une touche dédiée selon le modèle). Maintenez 3 à 5 secondes. La chaudière va relancer son cycle d'allumage. Si le code 611 disparaît et que la chaudière redémarre, le problème est résolu. S'il réapparaît rapidement, une cause interne persiste.</p>

