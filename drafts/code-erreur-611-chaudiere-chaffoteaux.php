<h2 id="signification">Que signifie le code erreur 611 sur une chaudière Chaffoteaux ?</h2>

<p>Le 611 est un code de sécurité lié au circuit d'évacuation des fumées, aussi appelé circuit de ventouse. Lorsque les capteurs de la chaudière détectent un tirage insuffisant ou une anomalie dans l'évacuation des gaz de combustion, la chaudière se verrouille et affiche ce code.</p>

<p>Le mécanisme est précis : après avoir détecté le défaut, la chaudière attend environ 12 minutes, puis tente de relancer son processus d'allumage. Si les conditions sont revenues à la normale, elle redémarre seule. Dans le cas contraire, elle se reverrouille. Si la chaudière reste bloquée sur 611, c'est qu'un problème physique persiste.</p>

<h3>Comment la chaudière détecte-t-elle ce défaut ?</h3>

<p>La chaudière est équipée de capteurs qui surveillent en continu le bon fonctionnement de l'évacuation. Ces capteurs réagissent à plusieurs situations : tirage insuffisant dans le conduit, obstruction partielle ou totale de la sortie, dysfonctionnement du ventilateur d'extraction, ou conditions météo défavorables comme le vent fort ou le gel.</p>

<h3>Erreur 611 vs erreur 612 : ne pas confondre</h3>

<p>Le code 612 signale un problème lié au ventilateur lui-même (rotation anormale ou arrêt complet). Le code 611 est plus large : il couvre tout défaut d'évacuation des fumées, dont le ventilateur peut être une cause mais pas la seule. Le code 108, lui, signale un défaut de pression d'eau — certains forums confondent le 611 avec un problème de pression, ce qui est inexact.</p>

