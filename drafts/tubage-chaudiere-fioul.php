<h2 id="obligation">Le tubage d'une chaudière fioul est-il obligatoire ?</h2>

<p>Ça dépend du type de chaudière — et la réponse est souvent oui.</p>

<p>Pour une <strong>chaudière fioul à condensation</strong>, le tubage est systématiquement obligatoire. Ce type de chaudière produit des condensats acides dans les fumées, qui attaquent les conduits maçonnés anciens. Un conduit de fumée non tubé ne résiste pas longtemps, et l'installation n'est pas conforme à la norme NF DTU 24.1.</p>

<p>Pour une <strong>chaudière fioul atmosphérique</strong> (sans condensation), le tubage n'est pas obligatoire si le conduit existant est en bon état, étanche, et aux bonnes dimensions. En pratique, sur des maisons de plus de 20 ans, les conduits sont presque toujours vétustes ou sous-dimensionnés — et le tubage s'impose de toute façon.</p>

<p>La norme NF DTU 24.1 est la référence légale pour la conception et l'installation. Elle encadre le choix du matériau, la hauteur de sortie de toit et les distances de sécurité. Toute intervention sur un conduit de fumée doit respecter ces exigences, faute de quoi l'assurance habitation peut refuser de couvrir un sinistre lié à l'installation.</p>

<h2 id="type">Quel type de tubage choisir pour une chaudière fioul ?</h2>

