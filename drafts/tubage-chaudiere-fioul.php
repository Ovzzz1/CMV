<p><strong>Si le tubage est inférieur au diamètre de sortie de la nouvelle chaudière, il doit être remplacé.</strong> Un réducteur de diamètre en aval du conduit est interdit — c'est une cause directe de tirage insuffisant et de refoulement des fumées dans le local.</p>

